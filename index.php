  // Function to update logo size
  function updateLogoSize() {
    const logoImg = document.querySelector("#header .logo img");
    const screenWidth = window.innerWidth;
    if (screenWidth <= 767) {
      logoImg.style.maxHeight = "30px";
    } else if (screenWidth >= 768 && screenWidth <= 1024) {
      logoImg.style.maxHeight = "47px";
    } else {
      logoImg.style.maxHeight = "60px";
    }
  }

  // Call the function on page load and window resize
  updateLogoSize();
  window.addEventListener("resize", updateLogoSize);

  // Function to handle header background color change on scroll
  window.addEventListener('scroll', function() {
    var header = document.getElementById('header');
    var navbarLinks = document.querySelectorAll('#navbar ul li a.nav-link');
    
    if (window.scrollY > 100) {
      header.style.backgroundColor = 'rgba(255, 255, 255, 0.7)';
      header.style.padding = '12px 0';
      // Change font color of navbar links to black
      for (var i = 0; i < navbarLinks.length; i++) {
        navbarLinks[i].style.color = 'black';
      }
    } else {
      header.style.backgroundColor = 'transparent'; // Remove the background color
      header.style.padding = '20px 0';
      // Change font color of navbar links to white
      for (var i = 0; i < navbarLinks.length; i++) {
        navbarLinks[i].style.color = 'white';
      }
    }
  });
</script>

